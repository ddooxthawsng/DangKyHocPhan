    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       // TODO add your handling code here:
       if(jTableHocPhan.getRowCount()==0){
             JOptionPane.showMessageDialog(null, "Danh sách rỗng");
        }
        else{
            String ma=txtMaHP.getText();
            HocPhan hp=new HocPhan(ma);
            if(li.contains(hp)){
                int chose=JOptionPane.showConfirmDialog(null, "Bạn chắc chắn muốn xóa học phần này?","Thông báo",
                        JOptionPane.YES_NO_OPTION);
                if(chose==JOptionPane.YES_OPTION){
                    li.remove(li.indexOf(hp));
                    try {
                        new HocPhanDAO().delHocPhan(ma);
                        JOptionPane.showMessageDialog(this, "Xóa thành công !", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    } catch (SQLException ex) {
                            JOptionPane.showMessageDialog(this, "Dữ liệu chưa thể xóa,hãy xóa dữ liệu lớp học phần của học phần này trước", "Thông báo", JOptionPane.ERROR_MESSAGE);
                    } catch (ClassNotFoundException ex) {
                            JOptionPane.showMessageDialog(this, "Dữ liệu chưa thể xóa,hãy xóa dữ liệu lớp học phần của học phần này trước", "Thông báo", JOptionPane.ERROR_MESSAGE);
                    }
                    LoadTable();
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Học phần có mã "+ma+" không có trong danh sách");
            }
        }

    }//GEN-LAST:event_jButton3ActionPerformed

