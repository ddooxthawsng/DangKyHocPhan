    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            if(txtMaHP.getText().equals(""))
                JOptionPane.showMessageDialog(null, "Mã học phần không được để trống");
            else if(txtTCLT.getText().equals("")||txtTCTH.getText().equals("")||txtTenHP.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Điền đầy đủ thông tin trước khi thêm");
            }
            else{
                mahp=txtMaHP.getText();
                tenhp=txtTenHP.getText();
                tclt=(int) Float.parseFloat(txtTCLT.getText());
                tcth=(int) Float.parseFloat(txtTCTH.getText());
                dongia=(int) Float.parseFloat(txtDGTC.getText());
                hinhthucthi=(String) jComboBoxHocPhan.getSelectedItem();
                HocPhan check=new HocPhan(mahp);
                HocPhan hp=new HocPhan(mahp,tenhp,hinhthucthi,tclt,tcth,dongia);
                if(li.contains(check)){
                    JOptionPane.showMessageDialog(null, "Mã học phần bị trùng, không thể thêm");
                }
                else{
                    HocPhan insert = new HocPhanDAO().addNew(hp);
                    li.add(hp);
                    LamMoi();
                    LoadTable();
                    JOptionPane.showMessageDialog(null, "Thêm thành công !");
                }
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Điền đầy đủ thông tin học phần trước khi thêm");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

