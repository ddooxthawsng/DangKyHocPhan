    private void XoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_XoaActionPerformed
        // TODO add your handling code here:
        if(jTableLHP.getRowCount()==0){
            JOptionPane.showMessageDialog(null, "Danh sách rỗng");
        }
        else{
            String ma=txtMaLHP.getText();
            LopHocPhan lhp=new LopHocPhan(ma);
            if(li.contains(lhp)){
                int chose=JOptionPane.showConfirmDialog(null, "Bạn chắc chắn muốn xóa lớp học phần này?","Thông báo",
                    JOptionPane.YES_NO_OPTION);
                if(chose==JOptionPane.YES_OPTION){
                    li.remove(li.indexOf(lhp));
                    LoadTable();
                    try {
                        new LopHocPhanDAO().deleteLopHocPhan(ma);
                        JOptionPane.showMessageDialog(this, "Xóa thành công !", "Thông báo", JOptionPane.INFORMATION_MESSAGE);

                    } catch (SQLException ex) {
                        JOptionPane.showMessageDialog(this, "Dữ liệu không thể xóa , sinh viên trong lớp học phần này đã có điểm!", "Thông báo", JOptionPane.ERROR_MESSAGE);
                    } catch (ClassNotFoundException ex) {
                        JOptionPane.showMessageDialog(this, "Dữ liệu không thể xóa, sinh viên trong lớp học phần này đã có điểm!", "Thông báo", JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Lớp học phần có mã "+ma+" không có trong danh sách");
            }
        }
    }//GEN-LAST:event_XoaActionPerformed

