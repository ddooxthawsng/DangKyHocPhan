    private void ThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ThemActionPerformed
        // TODO add your handling code here:
        try{
            if(txtMaLHP.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Mã lớp học phần không được để trống");
            }
            else if(txtMaHP.getText().equals("")||txtMaGV.getText().equals("")||txtTietHoc.getText().equals("")
                ||txtNgayBD.getText().equals("")||txtDiaDiem.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Điền đầy đủ thông tin của sinh viên trước khi thêm");
            }
            else{
                malophp=txtMaLHP.getText();
                mahp=txtMaHP.getText();
                magv=txtMaGV.getText();
                tiethoc=txtTietHoc.getText();
                thu=(String) jComboBoxThu.getSelectedItem();
                ngaybd=txtNgayBD.getText();
                diadiem=txtDiaDiem.getText();
                LopHocPhan check=new LopHocPhan(malophp);
                if(li.contains(check)){
                    JOptionPane.showMessageDialog(null, "Mã lớp học phần bị trùng,không thể thêm lớp học phần này ");
                }
                else{
                    LopHocPhan lhp=new LopHocPhan(malophp,mahp,magv,tiethoc,thu,ngaybd,diadiem);
                    LopHocPhan add=new LopHocPhanDAO().addNew(lhp);
                    LoadData();
                    JOptionPane.showMessageDialog(null, "Thêm thành công ");
                }
                LamMoi();
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Điền đầy đủ thông tin của sinh viên trước khi thêm");
        }
    }//GEN-LAST:event_ThemActionPerformed

